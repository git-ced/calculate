/**
 * @name square
 * @summary Gets the square of a number.
 *
 * @description Gets the square of a number.
 *
 * @example
 * // Normal usage
 * let result = sqaure(4);
 * // => 16
 *
 * @param {numberr} a number to be squared.
 * @returns {number}
 *
 * @function pure
 */
export default function square(a: number): number {
  return a ** 2;
}
