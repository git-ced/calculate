/**
 * @name cube
 * @summary Gets the cube of a number.
 *
 * @description Gets the cube of a number.
 *
 * @example
 * // Normal usage
 * let result = cube(2);
 * // => 8
 *
 * @param {numberr} a - number to be cubed.
 * @returns {number}
 */
export default function cube(a: number): number {
  return a * a * a;
}
